int		ft_str_is_alpha(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if ((str[i] >= 97 && str[i] <= 122)
			|| (str[i] >= 'A' && str[i] <= 'Z')
			|| (str[i] == '\0'))
		{
			i++;
		}
		else
		{
			return (0);
		}
	}
	return (1);
}
